# 2. Multiple If statements.

a=70000
if (a<200000):                                               #   ] ---> ALL
    print("The value of a is smaller than 200000")           #   ] ---> OF

if (a>8000):                                                 #   ] ---> THESE
    print("The value of a is greater than 8000")             #   ] ---> 'IF'

if (a>25000):                                                #   ] ---> PERFORM
    print("The value of a is greater than 25000")            #   ] ---> DIFFERENT

if (a>7000):                                                 #   ] ---> BECAUSE
    print("The value of a is greater than 7000")             #   ] ---> These are not connected to else , that's why these will not make 'LADDER'

if (a>85000):                                                #   ]
    print("The value of a is greater than 85000")            #   ] ------>  These two If and Else Will
else:                                                        #   ] ------>  Make Ladder known as 'If-Else' Ladder.
    print("The value of a is not greater than 5000 or 6000") #   ]

print("Done")

