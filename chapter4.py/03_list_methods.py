#Using "sort" method in lists to rearrange elements in increasing order
L1=[5,3,98,76,54,25]
L1.sort()
print(L1)

#Using "reverse" method in lists to reverse elements of a list
L1.reverse()
print(L1)


#Using "append" method in lists to add new element in a list
L1.append(8000)
print(L1)


#Using "insert" method in lists to change previous index value 
L1.insert(2,7500000)
print(L1) 


#using "pop" method in lists to remove elements from index value
L1.pop(2)
print(L1)


#Using "remove" method in lists to remove directly a certain element from the list
L1.remove(25)
print(L1)